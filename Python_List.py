numbers = ['130','100','140','150','190','200','250','200','230','250'] 
print(numbers)

numbers.sort() #Sort the items in the list in descending order.
print(numbers)

grade_3 = numbers[:4] #Fetch the item before the 4th index
print(grade_3)
grade_2 = numbers[4:7] #Sort the items index 4 and index 6 (before 7th index)
print(grade_2)
grade_1 = numbers[7:] #Fetches items after index 7
print(grade_1)



